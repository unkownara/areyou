export default function getRandomColor(char) {
    switch (char) {
        case 'a':
            return '#BECECD';
        case 'b':
            return '#DECBED';
        case 'c':
            return '#EFCBCB';
        case 'd':
            return '#DBBCBB';
        case 'e':
            return '#BCFEDF';
        case 'f':
            return '#FCBCBE';
        case 'g':
            return '#CEFFDD';
        case 'h':
            return '#DCCECF';
        case 'i':
            return '#CEBCBD';
        case 'j':
            return '#BECFFD';
        case 'k':
            return '#CCFCCD';
        case 'l':
            return '#CDDCCF';
        case 'm':
            return '#CDEDEB';
        case 'n':
            return '#EDCCDE';
        case 'o':
            return '#FDDDFC';
        case 'p':
            return '#CEEFEC';
        case 'q':
            return '#BCDFBD';
        case 'r':
            return '#CBEFFC';
        case 's':
            return '#EBBECC';
        case 't':
            return '#DEEEED';
        case 'u':
            return '#DBBDFC';
        case 'v':
            return '#FCBCBD';
        case 'w':
            return '#CFFCCC';
        case 'x':
            return '#FCDCCD';
        case 'y':
            return '#CBBECB';
        case 'z':
            return '#DFEFDD';
        default:
            break;
    }
}
